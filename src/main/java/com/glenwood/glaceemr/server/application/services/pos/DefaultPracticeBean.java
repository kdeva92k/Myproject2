package com.glenwood.glaceemr.server.application.services.pos;

/**
 * Default Practice details bean
 * @author software
 *
 */
public class DefaultPracticeBean {
	
	private String practiceDrMsg;
	private String practiceName;
	private String practiceStreet;
	private String practiceCity;
	private String practiceState;
	private String practiceZip;
	private String practiceWebAddress;
	private String practiceEmail;
	private String practicePhoneNum;
	private String practiceFaxNum;
	
	public String getPracticeDrMsg() {
		return practiceDrMsg;
	}
	public void setPracticeDrMsg(String practiceDrMsg) {
		this.practiceDrMsg = practiceDrMsg;
	}
	public String getPracticeName() {
		return practiceName;
	}
	public void setPracticeName(String practiceName) {
		this.practiceName = practiceName;
	}
	public String getPracticeStreet() {
		return practiceStreet;
	}
	public void setPracticeStreet(String practiceStreet) {
		this.practiceStreet = practiceStreet;
	}
	public String getPracticeCity() {
		return practiceCity;
	}
	public void setPracticeCity(String practiceCity) {
		this.practiceCity = practiceCity;
	}
	public String getPracticeState() {
		return practiceState;
	}
	public void setPracticeState(String practiceState) {
		this.practiceState = practiceState;
	}
	public String getPracticeZip() {
		return practiceZip;
	}
	public void setPracticeZip(String practiceZip) {
		this.practiceZip = practiceZip;
	}
	public String getPracticeWebAddress() {
		return practiceWebAddress;
	}
	public void setPracticeWebAddress(String practiceWebAddress) {
		this.practiceWebAddress = practiceWebAddress;
	}
	public String getPracticeEmail() {
		return practiceEmail;
	}
	public void setPracticeEmail(String practiceEmail) {
		this.practiceEmail = practiceEmail;
	}
	public String getPracticePhoneNum() {
		return practicePhoneNum;
	}
	public void setPracticePhoneNum(String practicePhoneNum) {
		this.practicePhoneNum = practicePhoneNum;
	}
	public String getPracticeFaxNum() {
		return practiceFaxNum;
	}
	public void setPracticeFaxNum(String practiceFaxNum) {
		this.practiceFaxNum = practiceFaxNum;
	}
	
}